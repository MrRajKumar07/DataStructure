package Core_Java;
import java.util.*;
public class InputOutput {
    public static void main(String[] args) {
        //Primitive DataType
        //Integer Value Input
        int x = 30;
        System.out.println("Integer:" + x);

        //Float Value Input
        float f=24.46f;
        System.out.println("Float:"+f);

        //Double Value Input
        double d=76.878;
        System.out.println("Double:"+d);

        //Long Value Input
        long l= 1234550576L;
        System.out.println("Long:"+l);

        //Boolean Value Input
        boolean b=true;
        System.out.println("Boolean:"+b);

        //Character Value Input
        char c='S';
        System.out.println("char:"+c);

        //Non-Primitive DataType
        //String Value Input
        String s="Raj Kumar";
        System.out.println("String:"+s);

        //Array Value Input
        String[] Branch={"CSE", "EEE", "ECE", "CSBS", "CTMA"};
        System.out.println("Array:"+Arrays.toString(Branch));


    }
}
